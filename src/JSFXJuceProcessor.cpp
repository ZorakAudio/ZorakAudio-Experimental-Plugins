    void importMidiToState (const juce::MidiBuffer& midiMessages, int numSamples)
    {
        st.currentBlockSize = numSamples;
        st.midiInCount = 0;
        st.midiInReadIndex = 0;
        st.midiOutCount = 0;
        st.midiInCountLastBlock = 0;
        st.midiOutCountLastBlock = 0;

        if (st.midiIn == nullptr || st.midiInCapacity <= 0)
            return;

        for (const auto metadata : midiMessages)
        {
            const auto msg = metadata.getMessage();
            if (msg.isSysEx())
                continue;

            if (st.midiInCount >= st.midiInCapacity)
            {
                ++st.midiInDropped;
                continue;
            }

            const auto* raw = msg.getRawData();
            const int rawSize = juce::jmin (msg.getRawDataSize(), 3);
            if (raw == nullptr || rawSize <= 0)
                continue;

            auto& ev = st.midiIn[st.midiInCount++];
            ev.sampleOffset = juce::jlimit (0, juce::jmax (0, numSamples - 1), metadata.samplePosition);
            ev.msg1 = (int) (juce::uint8) raw[0];
            ev.msg2 = (rawSize > 1) ? (int) (juce::uint8) raw[1] : 0;
            ev.msg3 = (rawSize > 2) ? (int) (juce::uint8) raw[2] : 0;
        }

        st.midiInCountLastBlock = st.midiInCount;
        st.midiInPeak = juce::jmax (st.midiInPeak, st.midiInCount);
    }

    void flushMidiFromState (juce::MidiBuffer& midiMessages)
    {
        if (st.midiOut == nullptr || st.midiOutCount <= 0)
            return;

        stableSortMidiEventsByOffset (st.midiOut, st.midiOutCount);
        st.midiOutCountLastBlock = st.midiOutCount;
        st.midiOutPeak = juce::jmax (st.midiOutPeak, st.midiOutCount);

        for (int i = 0; i < st.midiOutCount; ++i)
        {
            const auto& ev = st.midiOut[i];
            midiMessages.addEvent (makeJsfxMidiMessage (ev.msg1, ev.msg2, ev.msg3), ev.sampleOffset);
        }

        st.midiOutCount = 0;
    }

    bool pushParamsToStateSliders()
    {
        bool changed = false;
        const int varsCap = (int) (sizeof (st.vars) / sizeof (st.vars[0]));

        for (size_t i = 0; i < 64; ++i)
        {
            if (! sliderParamUsed[i])
                continue;

            const auto& info = sliderParamInfo[i];
            double newVal = st.sliders[i];

            // Robust value read:
            // - RangedAudioParameter::getValue() is ALWAYS normalised 0..1
            // - convertFrom0to1() ALWAYS returns the raw value in the parameter's declared range
            // This avoids accidentally feeding JSFX normalised values when the original slider
            // range was e.g. 0..100 or -24..+24.
            if (auto* v = paramAtomics[i])
            {
                // NOTE: For AudioParameterFloat, this returns the raw value in range.
                // For AudioParameterChoice, this returns the raw choice index.
                newVal = (double) v->load();
            }
            else if (auto* p = apvts->getParameter (info.pid))
            {
                const float norm = p->getValue();
                newVal = (double) p->convertFrom0to1 (norm);
            }

            // If the host-facing parameter is a CHOICE, its raw value is an index [0..N-1].
            // JSFX expects the original numeric slider value (min + index*step).
            if (info.isChoice)
            {
                const auto idx = (int64_t) std::llround (newVal);
                newVal = (double) info.min + (double) idx * (double) info.step;
            }

            // Clamp to the declared JSFX range.
            newVal = juce::jlimit<double> ((double) info.min, (double) info.max, newVal);

            // If this slider is not a CHOICE, emulate JSFX slider stepping.
            // This prevents host float jitter from triggering @slider rebuilds.
            if (! info.isChoice)
            {
                const double step = (double) (info.step > 0.0f ? info.step : 0.0f);
                if (step > 0.0)
                {
                    const double q = std::llround ((newVal - (double) info.min) / step);
                    newVal = (double) info.min + q * step;

                    // clamp again after quantize
                    newVal = juce::jlimit<double> ((double) info.min, (double) info.max, newVal);
                }
            }

            // Detect actual value change
            if (! lastSlidersValid || newVal != lastSliders[i])
            {
                changed = true;
                lastSliders[i] = newVal;
            }

            st.sliders[i] = newVal;

            // Slider alias support (sliderN:someVar=...): keep state var in sync with slider value.
            const int vIdx = sliderAliasVarIndex[i];
            if (vIdx >= 0 && vIdx < varsCap)
                st.vars[vIdx] = newVal;
        }

        lastSlidersValid = true;
        return changed;
    }

    struct GfxStateWrite
    {
        enum class Kind : uint8_t { Var = 0, Mem = 1 };
        Kind kind {};
        int32_t index = 0;
        double value = 0.0;
    };

    static constexpr uint32_t gfxWriteQueueSize = 8192u; // power of two
    static constexpr uint32_t gfxWriteQueueMask = gfxWriteQueueSize - 1u;
    static_assert ((gfxWriteQueueSize & (gfxWriteQueueSize - 1u)) == 0u, "gfxWriteQueueSize must be power of two");

    std::array<GfxStateWrite, gfxWriteQueueSize> gfxWriteQueue {};
    std::atomic<uint32_t> gfxWriteHead { 0 }; // UI thread producer
    std::atomic<uint32_t> gfxWriteTail { 0 }; // audio thread consumer

    bool enqueueGfxStateWrite (GfxStateWrite::Kind kind, int index, double value) noexcept
    {
        if (index < 0)
            return false;

        const uint32_t head = gfxWriteHead.load (std::memory_order_relaxed);
        const uint32_t next = (head + 1u) & gfxWriteQueueMask;

        if (next == gfxWriteTail.load (std::memory_order_acquire))
            return false; // full, drop

        gfxWriteQueue[head] = GfxStateWrite { kind, (int32_t) index, value };
        gfxWriteHead.store (next, std::memory_order_release);
        return true;
    }

    void applyQueuedGfxStateWrites() noexcept
    {
        uint32_t tail = gfxWriteTail.load (std::memory_order_relaxed);
        const uint32_t head = gfxWriteHead.load (std::memory_order_acquire);

        const int varsCap = (int) (sizeof (st.vars) / sizeof (st.vars[0]));

        while (tail != head)
        {
            const auto w = gfxWriteQueue[tail];

            if (w.kind == GfxStateWrite::Kind::Var)
            {
                if (w.index >= 0 && w.index < varsCap)
                    st.vars[(size_t) w.index] = w.value;
            }
            else // Mem
            {
                const int64_t mi = (int64_t) w.index;
                const int64_t logicalMemN = getGfxLogicalJsfxMemN (&st, jsfxDeclaredMaxMem);
                if (st.mem != nullptr && isGfxMirroredMemIndex (mi, logicalMemN))
                    st.mem[(size_t) mi] = w.value;
            }

            tail = (tail + 1u) & gfxWriteQueueMask;
        }

        gfxWriteTail.store (tail, std::memory_order_release);
    }

    static inline float clamp01f (float x) noexcept
    {
        return x < 0.0f ? 0.0f : (x > 1.0f ? 1.0f : x);
    }

    static inline float sgnf (float x) noexcept { return x >= 0.0f ? 1.0f : -1.0f; }

    // YSFX-style sqr mapping generalized for bipolar/unipolar.
    // We use modifier=2 for plain ":sqr".
    static float curveFrom01_sqr (float t, float lo, float hi, float modifier = 2.0f) noexcept
    {
        t = clamp01f(t);
        if (hi == lo) return lo;
        if (modifier <= 0.0f) modifier = 2.0f;

        const float inv = 1.0f / modifier;

        const float imax = sgnf(hi) * std::pow(std::abs(hi), inv);
        const float imin = sgnf(lo) * std::pow(std::abs(lo), inv);

        const float interp = t * (imax - imin) + imin;
        return sgnf(interp) * std::pow(std::abs(interp), modifier);
    }

    static float curveTo01_sqr (float v, float lo, float hi, float modifier = 2.0f) noexcept
    {
        if (hi == lo) return 0.0f;
        if (modifier <= 0.0f) modifier = 2.0f;

        // clamp v into range first
        if (v < lo) v = lo;
        if (v > hi) v = hi;

        const float inv = 1.0f / modifier;

        const float imax = sgnf(hi) * std::pow(std::abs(hi), inv);
        const float imin = sgnf(lo) * std::pow(std::abs(lo), inv);

        const float interp = sgnf(v) * std::pow(std::abs(v), inv);

        const float denom = (imax - imin);
        if (std::abs(denom) < 1.0e-20f) return 0.0f;

        return clamp01f((interp - imin) / denom);
    }

    static float curveFrom01_log (float t, float lo, float hi, float modifier) noexcept
    {
        t = clamp01f (t);
        if (hi == lo) return lo;

        // modifier == 0 -> classic exp interpolation (needs >0 domain)
        if (modifier == 0.0f)
        {
            if (lo <= 0.0001f || hi <= 0.0001f)
                return lo + t * (hi - lo);

            return lo * std::exp ((std::log (hi) - std::log (lo)) * t);
        }

        // modifier != 0 -> midpoint-based curve (works with lo==0)
        const float diff = hi - lo;
        if (std::abs (diff) < 1.0e-20f) return lo;
        if (std::abs (modifier - lo) < 1.0e-20f) return lo + t * diff;

        const float m = (modifier - lo) / diff;
        if (std::abs (m) < 1.0e-20f) return lo + t * diff;

        float mm1 = (m - 1.0f) / m;
        mm1 *= mm1;

        const float denom = (mm1 - 1.0f);
        if (std::abs (denom) < 1.0e-20f) return lo + t * diff;

        const float prefactor = diff / denom;
        return prefactor * (std::pow (std::abs (mm1), t) - 1.0f) + lo;
    }

    static float curveTo01_log (float v, float lo, float hi, float modifier) noexcept
    {
        if (hi == lo) return 0.0f;

        if (v < lo) v = lo;
        if (v > hi) v = hi;

        if (modifier == 0.0f)
        {
            if (lo <= 0.0001f || hi <= 0.0001f)
                return clamp01f ((v - lo) / (hi - lo));

            const float denom = (std::log (hi) - std::log (lo));
            if (std::abs (denom) < 1.0e-20f) return 0.0f;

            return clamp01f ((std::log (v) - std::log (lo)) / denom);
        }

        const float diff = hi - lo;
        if (std::abs (diff) < 1.0e-20f) return 0.0f;
        if (std::abs (modifier - lo) < 1.0e-20f) return clamp01f ((v - lo) / diff);

        const float m = (modifier - lo) / diff;
        if (std::abs (m) < 1.0e-20f) return clamp01f ((v - lo) / diff);

        float mm1 = (m - 1.0f) / m;
        mm1 *= mm1;

        const float base = std::abs (mm1);
        const float logBase = std::log (base);
        if (base <= 0.0f || std::abs (logBase) < 1.0e-20f)
            return clamp01f ((v - lo) / diff);

        const float inv_prefactor = (mm1 - 1.0f) / diff;
        const float inside = (v - lo) * inv_prefactor + 1.0f;
        if (inside <= 0.0f) return 0.0f;

        return clamp01f (std::log (std::abs (inside)) / logBase);
    }
